//   </span>
// </div>

function render (obj) {
  let dom = document.createElement(obj.tag)
  obj.attrs && Object.keys(obj.attrs).forEach(key => {
    dom.setAttribute(key, obj.attrs[key])
  })
  obj.children.length && obj.children.forEach(child => {
    dom.appendChild(render(child))
  })
  return dom
}

let obj = {
  tag: 'DIV',
  attrs:{
  id:'app'
  },
  children: [
    {
      tag: 'SPAN',
      children: [
        { tag: 'A', children: [] }
      ]
    },
    {
      tag: 'SPAN',
      children: [
        { tag: 'A', children: [] },
        { tag: 'A', children: [] }
      ]
    }
  ]
}

